package loops.whileLoop;

public class PrintNumbersAscending {
    public static void main(String[] args) {


        for (int i = 1; i <= 7; i++) {
            System.out.println(i);
        }

        int num = 1;
        while (num <= 7){
            System.out.println(num);
            num++;
        }
    }
}
